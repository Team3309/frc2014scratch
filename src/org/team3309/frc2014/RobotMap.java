package org.team3309.frc2014;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
    // For example to map the left and right motors, you could define the
    // following variables to use with your drivetrain subsystem.
    // public static final int leftMotor = 1;
    // public static final int rightMotor = 2;
    public static final int topleftmotor = 0;
    public static final int toprightmotor = 0;
    public static final int bottomleftmotor = 0;
    public static final int bottomrightmotor = 0;
    
    public static final int topleftsolenoid = 0;
    public static final int toprightsolenoid = 0;
    public static final int bottomleftsolenoid = 0;
    public static final int bottomrightsolenoid = 0;
    
    public static final int topleftencoderA = 0;
    public static final int toprightencoderA = 0;
    public static final int bottomleftencoderA = 0;
    public static final int bottomrightencoderA = 0;
    public static final int topleftencoderB = 0;
    public static final int toprightencoderB = 0;
    public static final int bottomleftencoderB = 0;
    public static final int bottomrightencoderB = 0;
    // If you are using multiple modules, make sure to define both the port
    // number and the module. For example you with a rangefinder:
    // public static final int rangefinderPort = 1;
    // public static final int rangefinderModule = 1;
}
